# What is the value of a, b, and c in the following program?

string = "Welcome to America!"
a = string[6]  # "e"
b = string[11]  # "A"
c = string[19]  # nil

=begin
The index number goes inside square brackets [ ] to get the value in that index position.  
When a given index position is higher than the string length, we get a return of nil.
=end